import sys

cur_pos = 0


file = open('locale_table.c', 'w')

file.write('extern const char locale_table[];')
file.write('const char locale_table[] = "')

def write_byte(val):
	file.write ('\\x%02x' % (val & 0xFF))
	
def write_string(s):
	for ch in s: 
		write_byte(ord(ch))
	
def list_get(lst, i):
	try: return lst[i]
	except: return 0
	
write_string("!x-sys-default-locale")
for x in locale_table:

	delta = x[1]-cur_pos; cur_pos = x[1]
	
	if delta >= (1<<14): write_byte((delta >> 14) | 0x80)
	if delta >= (1<<7): write_byte((delta >> 7) | 0x80)
	write_byte(delta & 127)

	write_byte((len(x[0]) & 15) | 
		((list_get(x, 2)>>10)<<4))
	write_string(x[0])

file.write('";')
